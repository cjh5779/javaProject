package variable;

public class VariableExample {
	public static void main(String[] args) {
		// 변수 선언
		int value;
		// 변수에 값을 저장
		value = 10;
		// 변수 선언 및 값 저장
		int result = value + 10;
		// 변수에 저장된 값 출력 (프로그램 실행 도중 변경 가능)
		System.out.println(result);
		value = 50;
		System.out.println(value);
		
		
	}

}

